// Reads db/migrations/meta/_journal.json + each .sql file, then writes
// db/migrations/index.ts with the journal and SQL strings inlined.
//
// Why: babel-plugin-inline-import is finicky with Metro (Expo SDK 54). This
// script bypasses bundler magic by emitting the SQL as plain string
// literals — works under any bundler, requires no plugin.
//
// Run as part of `npm run db:generate` (chained after drizzle-kit generate).

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const migrationsDir = join(here, '..', 'db', 'migrations');
const journalPath = join(migrationsDir, 'meta', '_journal.json');

const journal = JSON.parse(readFileSync(journalPath, 'utf-8'));

const lines = [
  '// AUTO-GENERATED by scripts/build-migrations.mjs — DO NOT EDIT.',
  '// Re-run `npm run db:generate` whenever the schema or migrations change.',
  '',
  `const journal = ${JSON.stringify(journal)};`,
  '',
  'const migrations: Record<string, string> = {',
];

for (const entry of journal.entries) {
  const sqlPath = join(migrationsDir, `${entry.tag}.sql`);
  const sql = readFileSync(sqlPath, 'utf-8');
  // Drizzle's expo-sqlite migrator looks up SQL by `m${idx}` zero-padded
  // to 4 digits — see drizzle-orm/expo-sqlite/migrator.js. The error
  // message prints `entry.tag` for humans, but the actual key is m-prefixed.
  const key = `m${String(entry.idx).padStart(4, '0')}`;
  lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(sql)},`);
}

lines.push('};');
lines.push('');
lines.push('export default { journal, migrations };');
lines.push('');

writeFileSync(join(migrationsDir, 'index.ts'), lines.join('\n'));
console.log(`✓ Wrote db/migrations/index.ts with ${journal.entries.length} migration(s) inlined`);
